package pe.edu.upeu;

public class Basicojava {

public void tablaMultiplicar() {
    for (int i = 1; i <= 10; i++) {
        for (int j = 1; j <= 10; j++) {
           System.out.println(i+" x "+j+" = "+(i*j)); 
        }
        System.out.println(" ");
    }
    
}


public void numerosprimos() {
    for (int i = 1; i <=20; i++) {
        for (int j = 1; j <20; j++) {
            System.out.println(i+" / "+j+" = "+(i/j));
            
        }
    }
    
}

}


